#include "pic.h"

void remap_pic()
{
    /* PIC1 - command 0x20, data 0x21
	PIC2 - command 0xA0, data 0xA1 */
	outb( 0x20, 0x11 );
	outb( 0xA0, 0x11 );
	outb( 0x21, 0x20 );
	outb( 0xA1, 0x28 );
	outb( 0x21, 0x04 );
	outb( 0xA1, 0x02 );
	outb( 0x21, 0x01 );
	outb( 0xA1, 0x01 );
	outb( 0x21, 0x00 );
	outb( 0xA1, 0x00 );
}

//Send an EOI to the master PIC
//IRQ0 - IRQ7
void master_eoi()
{
    outb(0x20, 0x20);
}

//Send an EOI to the slave EOI
//IRQ8+
void slave_eoi()
{
    outb(0xA0, 0x20);
}